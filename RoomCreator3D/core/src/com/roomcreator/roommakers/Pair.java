package com.roomcreator.roommakers;

/**
 * Класс, хранящий в себе реальные значения длины и ширины текстуры
 */
public class Pair {
    public float realMaterialWidth, realMaterialHeight;

    public Pair() {

    }

    public Pair(float realMaterialWidth, float realMaterialHeight) {
        this.realMaterialWidth = realMaterialWidth;
        this.realMaterialHeight = realMaterialHeight;
    }

}
